<p class="underline_head">Resume Tips: Five Ways to
Grab Employers' Attention</p>
<p class="general">With today's level of competition
for good jobs your resume has got only one chance to make a great first
impression. To be considered for interviews your resume must have that
special something that grabs the reader's attention and motivates them
to call you. Here are five strategies for transforming a blah document
into a WOW resume that will get employers calling you.</p>
<dl>
<di></di>
<p class="gen_footer">Keep your focus clear and to
the point</p>
<dd>The first thing potential employers need to know is
what you do and the position you are interested in. In the past job
seekers have used an objective statement at the top of their resume to
indicate their employment interest. With the lightning speed scanning
approach that recruiters take in viewing resumes, a wordy, vague
objective statement taking up three or more lines of text just doesn't
get the job done. In most cases they don't get read.<br>
Instead, write a short, direct professional summery that clearly
illustrates your career focus. Your statement should include your
profession, how long you've done it and your particular areas of
expertise. Something to the effect of:
<p class="emphasis">Senior purchasing
professions with 10 years' procurement expertise i<n: sourcing="" negotiation="" financial="" analysis="" strategic="" planning="" leadership="" contract="" law="" and="" process="" improvement=""></n:></p>
Remember, your resume is not an historical tell-all. To keep your focus
clear make sure that everything following in your resume relates to
your focus. Leave off extraneous details.</dd>
<di></di>
<p class="gen_footer">Stuff your resume with key
words.</p>
<dd>The more key words you use the more frequently your
resume will show up in online searches like LinkedIn, TheLadders and
CareerBuilder, etc.. Additionally, employer resume data bases also use
key words to query for qualifying candidates. Without appropriate key
words your resume will be electronically ignored. Without key words,
your resume is being shot off into a black void each time you submit it.<br>
A good way to make sure your resume is full of key words is to check it
against job postings. Use as many of the key words found in the
responsibilities and qualifications sections of job postings. As much
as you can, match up your terminology with what you find in job
postings.</dd>
<di></di>
<p class="gen_footer">Keep your resume
reader-friendly.</p>
<dd>Nothing gets ignored like a resume full of lengthy
blocks of text. No one has time to read through that much information.
Resume screeners need to be able to absorb your information quickly.
Leave out extraneous details so that key facts show up easily. Separate
blocks of text into smaller easy-to-digest snippets of information. Use
white space to separate bullet points so that each stand out. Be sure
that your font size is readable: nothing smaller than 11 point.</dd>
<di></di>
<p class="gen_footer">Include plenty of
accomplishments.</p>
<dd>If you want to stand out from the crowd you must
include accomplishments throughout your resume. Write accomplishments
that show how you solve universal problems such as saving time, cutting
costs, improving performance and increasing customer satisfaction. Your
accomplishments should stand out on your resume in bullets separate
from your responsibilities. Don't make the common mistake of combining
responsibilities and accomplishments in a long list of bullets. List
your responsibilities in a small block of text and your accomplishments
in bullet form following.</dd>
<di></di>
<p class="gen_footer">Get your best information on
page one.</p>
<dd>It's true, if you can't grab their attention on page
one they won't stick it out to find out the wonderful things you've got
on page two or three. This presents a problem for those who experienced
their most productive work five or more years back. The solution is to
use the hybrid resume format that allows you to create a highlight of
accomplishments section at the top of page one of your resume. This
area of your resume is reserved for the best examples of your work. The
accomplishments you include should illustrate the key transferable
skills needed for the position you are interested in.</dd>
</dl>
<p class="general">Don't delay in implementing these
resume changes. Employers are waiting for you with opportunities for a
better career and a better life.</p>
