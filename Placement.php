<p class="emphasis">So Why Should We Hire You?&#8221;</p>
<p class="general">If you are currently in a job
search chances are
you've been asked that question already. Undoubtedly, it is the most
feared
interview question, but one of the most common. It pays to be ready to
answer
it. Helps to understand that the question is an invitation for you to
sell
yourself. This is a good thing. No one is going to hire you until they
have
been sold on you. This is your chance to state your value to the
prospective
employer. The best way to answer this question is to prepare for it
like a
sales person. There are three steps to selling yourself with confidence.</p>
<p class="gen_footer">Know your product &#8220;YOU.&#8221;</p>
<p class="general">Every successful salesperson
knows their product
inside and out. They understand the benefits of each product feature.
In like
manner, you must be able to articulate your transferable skills. First,
take
inventory of your skills. Make sure the skills you focus on are in
demand for
the position you seek. Next, take stock of the times of crisis when
you've used
those skills to solve problems. Finally, ask yourself what your
employer got
out of your successes on the job. Did you save time or money, increase
revenue,
improve service or increase productivity? Your success stories carry
more weight
when you can quantify the results. These success stories make up your
selling
points.</p>
<p class="gen_footer">Know the challenges of the
position.</p>
<p class="general">Before you can tell them why they
should hire you, you
must understand their current challenges. After all, you couldn't sell
a car
unless you knew understood how it was to be used. Until you know what
challenges go with the position you won't know which of your selling
points to
talk about. To learn about their challenges you must ask them.<br>
In the beginning of the interview ask your
interviewer, &#8220;What challenges do you see as most significant for this
position in the first six months?&#8221; Take careful note of his/her
response.
You will learn the &#8220;hot button&#8221; issues that you must sell you.</p>
<p class="gen_footer">Match your skills to their
challenges</p>
<p class="general">Here is where you get to sell
yourself. Once you
understand the critical skills they need for the job you simply share
with them
your success stories of when you have faced similar problems and how
you solved
them. Be sure to include the all-important benefit your company
received. Start
off your value statements with phrases like:</p>
<p class="emphasis">&#8220;I found a significant savings
opportunity when...&#8221;<br>
&#8220;My team gained efficiency when I discovered how to...&#8221;<br>
&#8220;My boss achieved his quarterly objective when I...&#8221;</p>
<p class="general">Remember, even if you don't get
asked &#8220;why
should we hire you&#8221; it is the underlying question and the point of the
whole interview. Job interviews are your chance to sell your skills,
talents
and expertise. Before your next interview practice good salesmanship
and
prepare to sell yourself like a pro.</p>
