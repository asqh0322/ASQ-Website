<h3>A note about our Dinner Meetings:</h3>

<h4>DINNER RESERVATIONS / CANCELATIONS</h4>
<h4 class=emphasis>Note change in policy</h4>

<p class=general>Section 503 is now asking all Members and Guests to PRE-REGISTER on-line.<br/><br/>
If you make a reservation to attend a dinner meeting and find later that you are unable to attend, due to an emergency, kindly let us know 48 hours in advance or sooner if possible, and the Section will endeavor to refund your payment.  However, if you pre-register and then fail to attend, your prepayment will be forfeited, since the Section is required to pay the hotel venues for the meal, plus tax and gratuity (normally subsidized by the Section).</p>
<h2>If you must cancel, please give at least 48 hours notice by e-mail to <a href="mailto:registration@asq-harrisburg.org?Subject=Registration" target="_top">registration@asq-harrisburg.org</a>. (Notice must be given prior to 4:00 PM)</h2>

<h4>MEETING ETIQUETTE</h4>

<p class=general>Dear Member</p>

<p class=generalThe Executive Committee wants your experience at each of our dinner meetings to be the best that it can be; in fact, we want to guarantee the excellence of
the food and service quality no matter what venue we are using for Section meetings and presentations.</p>

<p class=general>Please inform any Officer or Executive Committee member if a problem arises for you during the meeting and we will address your concern
immediately.  Please tell us, if:</p>

<ul><li>The meal is not cooked properly</li>
<li>The meeting room is too hot or too cold</li>
<li>Something is missing from your table that needs replaced; and, or</li>
<li>There is service problem of any kind.</li>
</ul>

<p class=general>We really want to know as soon as possible during the meeting rather than waiting until the end of the meeting and/or finding out on the comments form that
there was a problem or issue which we could have ameliorated on the spot.</p>

<p class=general>Thank you!</p>

<p class=general>Your Executive Committee</p>

