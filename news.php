  <tr><td class=tdborder><a name=Gantt></a><p class=section_header>Gantt Chart</p>
  <p class=general>Also Called: milestones chart, project bar chart, activity chart.</p>
  <p class=underline_head>Description</p>
  <p class=general>A Gantt chart is a bar chart that shows the tasks of a
  project, when each must take place and how long each will take. As the
  project progresses, bars are shaded to show which tasks have been completed.
  People assigned to each task also can be represented.</p>
  <p class=underline_head>When to Use Gantt Charts</p>
   <ul id=disc>
   <li>When scheduling and monitoring tasks within a project.</li>
   <li>When communicating plans or status of a project.</li>
   <li>When the steps of the project or process, their sequence and their duration are known.</li>
   <li>When it&#8217;s not necessary to show which tasks depend on completion of previous tasks.</li>
   </ul>
  <p class=underline_head>Gantt Chart Basic Procedure</p>
  <p class=gen_header>Construction</p>
  <ol class=number>
   <li>Identify tasks:</li>
   <dl>
    <dd>Identify the tasks needed to complete the project.</li>
    <dd>Identify key milestones in the project by brainstorming a list, or by drawing a <a
        href="http://www.asq.org/learn-about-quality/process-analysis-tools/overview/flowchart.html">flowchart</a>,
        storyboard or <a
        href="http://www.asq.org/learn-about-quality/new-management-planning-tools/overview/arrow-diagram.html">arrow
        diagram</a> for the project.</li>
    <dd>Identify the time required for each task.</li>
    <dd>Identify the sequence: Which tasks must be finished before a following task can begin, and which can happen simultaneously? Which tasks must be
        completed before each milestone?</li>
   </dl>
   <li>Draw a horizontal time axis along the top or bottom of a page. Mark it off in an appropriate scale for the length of the tasks (days or weeks).</li>
   <li>Down the left side of the page, write each task and milestone of the project in order. For events that happen at a point in time (such as a
       presentation), draw a diamond under the time the event must happen. For activities that occur over a period of time (such as developing a plan
       or holding a series of interviews), draw a bar that spans the appropriate times on the timeline: Align the left end of the bar with
       the time the activity begins, and align the right end with the time the activity concludes. Draw just the outlines of the bars and diamonds;
       don&#8217;t fill them in.</li>
   <li>Check that every task of the project is on the chart.</li>
  </ol>
  <hr class=gray></hr>
  <p class=gen_header>Using the Chart</p>
  <ol start=5 type=1>
   <li>As events and activities take place, fill in the diamonds and bars to show completion. For tasks in progress, estimate how far along you are and fill in that much of the
       bar.</li>
   <li>Place a vertical marker to show where you are on the timeline. If the chart is posted on the wall, for example, an easy way to show the current time is with a heavy dark
       string hung vertically across the chart with two thumbtacks.</li>
  </ol>
  <hr class=gray></hr>
  <p class=gen_header>Gantt Chart Example</p>
  <p class=general>The figure below shows a Gantt chart used to plan a
  benchmarking study. Twelve weeks are indicated on the timeline. There are two
  milestone events, presentations of plans for the project and for the new process
  developed in the study. The rest of the tasks are activities that stretch
  over periods of time. </p>
  <p class=center><img class=imggraphic src="news_files/image002.jpg" alt="Gantt Chart Example", alt="Gantt Chart Image"></p>
  <p class=gen_header>Gantt Chart Example</p>
  <p class=general>The chart shows the status at Thursday of the sixth
  week. The team has finished seven tasks through identifying key practices,
  measures and documentation.</p>
  <p class=general>This is a hectic time on the project, with three
  time-consuming activities that must happen simultaneously:</p>
  <ul id=circle>
    <li>The team estimates it is
       one-fourth finished with identifying benchmark partners and scheduling
       visits; one-fourth of that bar is filled.</li>
    <li>Team members have not yet
       begun to identify the current state. </li>
    <li>They are halfway through
       collecting public data, which puts them slightly ahead of schedule for
       that task. </li></ul>
  <p class=general>They are behind schedule for the first two of these
  tasks and ahead of schedule for the third. Perhaps they need to reallocate
  their workforce to be able to cover the three activities simultaneously. </p>
  <p class=general>There is a fourth activity that could be happening now
  (develop benchmark questions), but it is not urgent yet. Eventually the team
  will have to allocate resources to cover it too, before visits can begin. </p>
  <hr class=gray></hr>
  <p class=gen_header>Gantt Chart Considerations</p>
  <ul id=circle>
   <li>Sometimes Gantt charts are drawn with additional columns showing details such as the amount of time the task is expected
      	to take, resources or skill level needed or person responsible.</li>
   <li>Beware of identifying reviews or approvals as events unless they really will take place at a specific time, such as a 
	meeting. Reviews and approvals often can take days or weeks.</li>
   <li>The process of constructing the Gantt chart forces group members to think clearly about what must be done to accomplish 
	their goal. Keeping the chart updated as the project proceeds helps manage the project and head off schedule problems.</li>
   <li>It can be useful to indicate the critical points on the chart with bold or colored outlines of the bars.</li>
   <li>Computer software can simplify constructing and updating a Gantt chart.</li>
  </ul>
  <p class=emphasis>Excerpted from Nancy R. Tague&#8217;s 
	<a href="http://www.asq.org/quality-press/display-item/index.html?item=H1224" target="_blank">The Quality Toolbox</a>,
	 Second Edition, ASQ Quality Press, 2004, pages 271-274.</p>
