<tr><td class=tdborder><a name=QFD></a><p class=section_header>QFD &#8211; Under One Roof</p>
  <p class=general>Quality function deployment (QFD) is a great tool for designing a
  business based on the voice of the customer. Many quality tools are
  multifaceted. Here is how QFD and its house of quality can be used to better
  schedule and scope audits.</p>
  <p class=general>One of the guiding principles of
  quality is cross functionality. This principle is embedded in many current
  quality management standards. For instance, ANSI/ISO/ASQ Q9001-2000 says the
  following:</p>
  <p class=emphasis>&#8220;The application of a
  system of processes within an organization, together with the identification
  and interactions of these processes, and their management, can be referred to
  as the &#8216;process approach.&#8217;</p>
  <p class=emphasis>&#8220;An advantage of the
  process approach is the ongoing control that it provides over the linkage
  between the individual processes within the system of processes, as well as
  over their combination and interaction.&#8221;</p>
  <p class=general>How do you audit interaction? In
  Figure 1&#8217;s example, the columns are the general departments at most
  companies (or they could be specific processes). The roof becomes all the
  interactions between any two departments.</p>
  <p class=center><img class=imgsquare src="news_files/image005.jpg" alt="QFD House"></p>
  <p class=general>There are eight departments in
  this example, making the number of two-way interactions 28 (8C2). With eight departments,
  the roof would be made up of 28 interactions. Is it necessary to audit all 28
  interactions? Probably not. You can always do a Pareto chart and audit the
  six vital few interactions. Which six? Probably by consensus we can agree on
  four or five interactions of the six vital few.</p>
  <p class=general>Not only do we have four bad
  (&#8211;) interactions, we also have four good (+) ones. You can audit for
  objective evidence of the bad interactions to begin a process of determining
  root cause and implementing effective corrective action. We can audit the
  positive interactions to collect &#8220;what we do well&#8221; information
  (the appreciative inquiry method).</p>
  <p class=general>For example, you can audit the
  bad interaction between the engineering and production departments to
  objectively shed light on the problem. You can audit how the engineering
  department produces and delivers standards to the production department. You
  can audit the production department on how useful it finds the engineering
  department&#8217;s standards. A revealing exercise is to see if the supplier
  output is equal to the customer input, as illustrated in Figure 2.</p>
  <p class=center><a href="http://www.asq.org/img/qp/075605_figure2.gif" target="_blank">
   <img class=imgsquare src="news_files/image007.jpg" alt="Figure 2"></a>
  <p class=general>As a positive example, you can
  audit the interaction between the HR and production departments to identify
  something you want to continue and perhaps replicate. You can audit HR on how
  it hires production workers. Perhaps you would find that HR goes to observe
  the actual work environment. You can audit how the production
  department&#8217;s new hires perform. Per-haps you would discover that HR
  receives feedback one month later on how a new hire is working out.</p>
  <p class=general>Auditing interactions would
  require you to expand the scope of audits to include more than one department
  or process. Auditing several departments or processes might sound impossible
  with time and budget constraints. But remember, scope down audits of
  suppliers to a specific commodity in the main process you are auditing. If
  nothing else, make sure all your audits cover at least two departments or two
  processes. You must begin auditing the way the business actually
  operates&#8212;a system of processes, including interactions.</p>
  <p class=general>Another use of the roof is to
  determine how to build cross functional teams, perhaps to tackle a Six Sigma
  project. If you know there is a weak relationship between two functions, why
  not put representatives from these areas on the same team with a single
  purpose? I have seen a weak relationship become strong through a team
  building process. Moreover, this strong team relationship might have a
  spillover effect on day-to-day business operations.</p>
  <p class=general>Whether forming cross functional
  teams or auditing process interactions, you will provide value added
  information for management review of the effectiveness of the quality system.
  Using the house of quality for cross functionality and interactions makes
  sense.</p>
  <p class=general>After all, aren&#8217;t your
  departments and processes under one roof?</p>
