<tr><td class=tdborder><a name=Garden></a><p class=section_header>Quality professionals must cultivate success
  in their &#8216;organizational gardens&#8217;</p>
  <p class=emphasis>--Excerpt from Quality Progress, November 2008&#8212;</p>
  <p class=general>Quality professionals are constantly confronting practical questions that are always specific to the 
	organizations they serve: How do we grow our quality efforts? How can I keep my organization&#8217;s approach to 
	quality vibrant? How do I keep the leadership focused on quality? Should we be changing the focus of our quality 
	program? How do I transplant a successful quality endeavor from one part of the organization into another?</p>
  <p class=general>Many quality professionals understand that the answers to these questions require the ability to envision
	their organizations as living entities, existing within their understanding of systems theory. This requires quality 
	professionals to function along the lines of organizational gardeners who cultivate their organizations so they can 
	produce beauty on many levels.</p>
  <p class=underline_head>A basic approach</p>
  <p class=general>Regardless of whether an individual is an organizational gardener in a manufacturing, healthcare, service, government,
	education or not-for-profit setting, the task of tending to an organization can be difficult because it&#8217;s easy to lose sight of 
	four basic gardening principles:</p>
  <dl>
    <di><p class=gen_footer>Expect the seasons</p></di>
      <dd>Start with the premise that everything changes and that no action you or your organization takes will ever be 
	permanent. Your task is to study your organization as it exists right now, to think about how it can be improved, and 
	then to perform the necessary pruning, spraying, transplanting and other actions.</dd>   
    <di><p class=gen_footer>It is all an interconnected ecosystem</p></di>
      <dd>Each organization is a complex system of interconnected parts that exists within an even larger ecosystem of
	  social, economic and political conditions. The term &quot;unintended consequences&quot; is just another way of 
	  saying we didn&#8217;t think things through from a systems perspective before we implemented change.</dd>
    <di><p class=gen_footer>Don&#8217;t spray everything</p></di>
      <dd>Just because you own a set of garden tools does not mean you are a gardener. It is important to have a variety of 
	  tools and even more important to know when to use them and when not to use them. Don&#8217;t spray the herbicide on 
	  everything in sight just because you have it.<br><br> Most quality professionals have experienced a time when their 
	  organization went overboard with a particular tool and attempted to apply it in an uncritical manner. This causes 
	  cynicism about quality simply responding to the fad of the month.</dd>
    <di><p class=gen_footer>Get dirty</p></di>
      <dd>Organizational gardening requires a lot of hard work and the mastery of a complex body of knowledge (BoK). This 
	  mastery only comes through a process known as praxis, in which we use our understanding of theory to inform our 
	  practice and use our practical experiences to reflect on and refine our understanding of theory.</dd>
  </dl>
  <p class=underline_head>Alter your perception</p>
  <p class=general>We sometimes get in a rut when it comes to how we
  approach organizational issues and the perspective from which we understand
  organizational gardening. Research into how the mind functions suggests our
  perceptions about quality and our preferences for approaches might be
  influenced by our brain preference, leading us to ask whether we are
  left-brained or right-brained gardeners.</p>
  <p class=general>For the
  purposes of helping quality professionals think about getting dirty as
  organizational gardeners, it could be useful to look at quality methods
  simultaneously from two dimensions. One dimension would organize principles
  and methods according to whether they establish and promote order or whether
  they engender change, as Whitehead might suggest. The other dimension
  considers whether the principles and methods are linear and orderly (the
  left-brain preference) or relational in terms of complex systems (the
  right-brain preference). Figure 1 provides a matrix of the BoK from this
  perspective.</p>
  <p class=center><img class=imgsquare src="news_files/image003.gif" alt="Org Gardening BOK">>/p>
  <p class=general>The greatest challenge for the quality practitioner as
  organizational gardener might be facilitating the movement from one quadrant
  to another when the needs of the organization require a change in thinking
  and action. While the detailed, day-to-day digging in the organizational dirt
  in the conformance quadrant is essential, it is equally important at times to
  move over to the assessment quadrant and evaluate the relative beauty of the
  garden and decide what to uproot, trim or fertilize next.</p>
  <p class=general>When it comes to promoting change, quality professionals
  show a marked preference for working in the orderly change quadrant.
  Remember, the orderly introduction of change (improvement) needs to be
  balanced by the work in the conformance and assessment quadrants. So where
  does the right-brained, relational approach to promote change fit in?</p>
  <p class=underline_head>Ethical dilemma</p>
  <p class=general>When quality professionals are dealing with macro-level
  quality issues in their organizations while functioning as organizational
  gardeners, there are some ethical considerations to ponder.</p>
  <p class=general>When working within a system, there is no neutrality.
  Quality practitioners cannot park themselves in a safe, neutral part of the
  system. That&#8217;s because they are part of the system. From
  Whitehead&#8217;s perspective, every action we take is either going to
  promote greater order or promote change.</p>
  <p class=underline_head>Don&#8217;t be afraid to dig in</p>
  <p class=general>There is no shortage of quality practitioners who can
  conduct an audit, lead a group through a Six Sigma process improvement
  routine or plot control charts, even though these specific areas require
  expert skill and knowledge. Today&#8217;s challenge goes back to the issues
  that prompted Philip Crosby to establish the Quality College, that motivated
  Joseph Juran to establish the Juran Center, and that called Deming to teach
  countless workshops at George Washington University.</p>
  <p class=general>All three of these quality leaders were trying to help
  everyone see quality from a systems perspective and impart a breadth of
  understanding that could enable us to nurture and grow quality in organizations
  for the betterment of society. The garden is calling, and it won&#8217;t
  wait. You probably have some organizational gardening of your own to do. Dig
  in.</p>
