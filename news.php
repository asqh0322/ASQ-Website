<tr><td class=tdborder><a name=RCA></a><p class=section_header>Root Cause Analysis&#8212;Not Always an Answer</p>
  <p class=general>One of the
  more useful and popular methods used by quality professionals is root cause analysis
  (RCA). But, there are instances in which RCA isn&#8217;t the best choice.</p>
  <p class=general>One such
  situation is when working to reduce the chance and cost of recalls and
  especially product liability lawsuits, according to Randall Goodden, chair of
  ASQ&#8217;s Product Safety and Liability Prevention Interest Group for the
  past 12 years (the group was established in 1981).</p>
  <p class=general>Goodden says
  RCA and failure mode and effects analysis, which is in the same category,
  help design engineers not only determine why a product might not last as long
  as intended, but also predict what could fail.</p>
  <p class=general>&#8220;In
  those cases, you&#8217;re looking more at product reliability. But,
  preventing hazards that will ultimately lead to recalls and possible lawsuits
  requires a different type of analysis,&#8221; Goodden explains. &#8220;This
  analysis is much more in-depth than RCA and includes all the ways the product
  is likely to be used, as well as misused,&quot; and how anyone who comes into
  contact with the product could be injured.</p>
  <p class=general>Another point
  Goodden makes about RCA and FMEA is that they&#8217;re commonly conducted by
  design engineers on <em>their own</em> designs. But, the unfortunate fact,
  according to Goodden, is that the majority of products that are being
  recalled involve issues related to design defects. </p>
  <p class=general>&#8220;Prevention
  of design defects requires an independent analysis of what would lead to
  faulty designs,&#8221; says Goodden. &#8220;Product safety teams should be
  made up of a small group of specially trained people, such as safety and
  quality engineers, who have a very in-depth view as to how a product will be
  used and who will use it. They must analyze the proposed design on behalf of
  the entire company and become specialists in this area.&#8221; </p>
  <p class=general>Goodden
  believes product safety teams should be comprised of these independent
  positions, as opposed to manufacturing or engineering personnel, that are
  likely to face timeframe pressures.</p>
  <p class=general>What tools are
  available to product safety teams? In addition to software packages that
  perform hazard analyses and risk assessments, Goodden says these teams can use
  data from previous products, including customer feedback, warranty returns,
  and customer returns from the field.</p>
  <p class=general>Brainstorming
  sessions and checklists that feature previous problems with a product are
  also quite helpful. Most importantly, however, Goodden says product safety
  team members need to have special training in how to recognize potential
  hazards.</p>
  <p class=general>&#8220;RCA and
  FMEA help determine whether a product will be reliable and do what it is
  intended to do, and lead to satisfied customers,&#8221; concludes Goodden.
  &#8220;Hazard analysis and risk assessment by a product safety team will go
  beyond just ensuring the product will be reliable and live up to the
  customer's expectations, by reducing the chance and cost of recalls that
  could then lead to product liability lawsuits.&#8221;</p>
  <p class=emphasis>Goodden is
  principle of Randall Goodden International and Goodden Enterprises LLC and
  the author of <em>Lawsuit! Reducing the Risk of Product Liability for
  Manufacturers</em> (Wiley, 2009).<br/>For those
  interested in learning more about this topic, contact Randall Goodden at <a
  href="mailto:randygoodden@yahoo.com">randygoodden@yahoo.com</a>.</p>
