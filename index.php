<td colspan="2" valign="top"><p>As you begin to plan for the rest of 2017, consider
planning internal audits with some of the following
suggestions to ensure they are effective:
<ol>
<li>Use a qualified auditor: Inexperienced internal
auditors will often not dive deep enough to truly
understand if a process is effective. Ensure the
individual is outside of the system that they are
auditing and that they understand that this process is to
help the system and not penalize employees.</li>
<li>Be careful with checklists: Checklists can be
helpful tools in completing an audit but can often lead
to just filling in a box without appropriate probing
questions. Process audits often lead outside of the area
being audited which can make checklists difficult to
maintain.</li>
<li>Not including customer specifications or
requirement: Utilize this as an opportunity to ensure
you are meeting your customerâ€™s quality requirements
not only the quality standard to which you may be
registered.</li>
<li>Not diving deep enough: ISO Auditors will only
be able to look at higher level documents and a
sampling of records. Internal auditors have more time
and knowledge of the system allowing them to review
in detail documents, work instructions and quality
records.</li>
<li>Findings are not a bad thing: If you identify an
issue and take appropriate action during the auditing
process your 3rd part auditor will likely not write a
finding during their visit.</li>
<li>Not asking enough questions: Internal Audits are
designed not only to ensure that you are meeting
quality standards and your own procedures but also to
take the opportunity to review a process and make it
better.</li>
<li>Not involving enough staff: Internal Audits can
serve as training for employees that are outside of the
system being audited. Diversified employees are
critical to growing companies and if your staff has an
understanding how the other processes work they can
make companywide improvements.</li>
</ol>

Please also plan to join us as we are continuing our
speaker series with a tour of an aquaponics facility in
April, a review of organization knowledge in relation
to social media in May and the annual awards banquet
in June. The executive committee is continuing to
look for new opportunities to provide services to both
current and potential members for our organization
through website updates, emails and new programs
and look forward to seeing you at our events.


</p><p>Adam Marsh</p>
